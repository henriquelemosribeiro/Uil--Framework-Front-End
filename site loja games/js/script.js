// Exemplo mínimo de JavaScript

document.addEventListener("DOMContentLoaded", function () {
    // Aguarde o documento ser completamente carregado antes de interagir com os elementos da página

    // Selecione o botão pelo ID (substitua "myButton" pelo ID do seu botão)
    var button = document.getElementById("myButton");

    // Adicione um evento de clique ao botão
    button.addEventListener("click", function () {
        // Ação a ser executada quando o botão é clicado
        alert("Site em construção!");
    });
});
